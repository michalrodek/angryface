export async function getData(serviceName: string, level: number) {
  const resp = await fetch("https://diskuze.seznam.cz/graphql", {
    next: {
      revalidate: 300,
    },
    method: "POST",
    headers: {
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      query: `query Discussions {
        discussions(sort: CREATED_DATE_DESC, serviceName: "${serviceName}", first: 30) {
          edges {
            node {
              id
              title
              createdDate
              canonicalString
              commentsCount
              comments(sort: SCORE_DESC, level: ${level}) {
                edges {
                  node {
                    id
                    content
                    createdDate
                    reactionsCount {
                      count
                      reactionType {
                        name
                      }
                    }
                    user {
                      profilName
                      profilLink
                      profilImage
                    }
                  }
                }
              }
              service {
                title
              }
            }
          }
        }
      }
      `,
    }),
  });

  const data: Query = await resp.json();

  return data;
}
